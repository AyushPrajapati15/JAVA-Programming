public class O2_For_loop {
    public static void main(String[] args) {
//        For loop
        int i;
        for (i=0;i<=5;i++){
            System.out.println(i);
        }
        int n=5;
        for (i=n;i>0;i--){
            System.out.println(i );
        }

//        first 10 even and odd number
        for (i=1;i<=5;i++){
            System.out.println(2*i);
        }
        for (i=1;i<6;i++){
            System.out.println(2*i+1);
        }
    }
}

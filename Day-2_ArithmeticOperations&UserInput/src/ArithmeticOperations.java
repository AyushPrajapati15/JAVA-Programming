public class ArithmeticOperations {
    public static void main(String[] args) {

        int num1 = 50;
        int num2 = 40;
        int num3 = 60;
        int sum,minus,prod,div;
        sum=num1+num2+num3;
        System.out.println("The sum of these number is :-");
        System.out.println(sum);
        minus = num3 - num1;
        System.out.println("The minus of num1 & num3 is :-");
        System.out.println(minus);
        prod = num1 * num2 * num3;
        System.out.println("The product of num1, num2, num3 is :-");
        System.out.println(prod);
        div = num3 / num1;
        System.out.println("The division of num1 & num3 is :-");
        System.out.println(div);



    }
}
